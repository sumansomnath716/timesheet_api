class Technology{

           constructor(
                    id,
                    tech_name,
                    tech_type,
                    tech_dtls,
                    status,
                    created_by,
                    created_at,
                    updated_by,
                    updated_at,
           ){
                    this.id = id;
                    this.tech_name = tech_name;
                    this.tech_type = tech_type;
                    this.tech_dtls = tech_dtls;
                    this.status = status;
                    this.created_by = created_by;
                    this.created_at = created_at;
                    this.updated_by = updated_by;
                    this.updated_at = updated_at;  
           }

}